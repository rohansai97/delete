
public class Shifting {
	public static void main(String[] args) {
		System.out.println(5 << 2);// this is left shifting it comes like 5 binary value
		// is 101 by adding 2 zeros in the right side so value comes as 10100 for that
		// value is 20

		System.out.println(20 >> 3);// in this cancel last 2 digits so 10100 becomes 101
		// so value becomes 101 i.e, 5

	}

}

